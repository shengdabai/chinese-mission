/** Find a single mission by its id (e.g. "m1") or code (e.g. "order_hot_latte") */
export function getMission(missionIdOrCode: string): Mission | undefined {
  for (const scenario of scenarios) {
    const found = scenario.missions.find(
      (m) => m.id === missionIdOrCode || m.code === missionIdOrCode,
    );
    if (found) return found;
  }
  return undefined;
}

/** Find a single scenario by its id (e.g. "s1") or code (e.g. "cafe_restaurant") */
export function getScenario(scenarioIdOrCode: string): Scenario | undefined {
  return scenarios.find(
    (s) => s.id === scenarioIdOrCode || s.code === scenarioIdOrCode,
  );
}

/** Get all missions for a given category */
export function getMissionsByCategory(
  category: Scenario["category"],
): Mission[] {
  return scenarios
    .filter((s) => s.category === category)
    .flatMap((s) => s.missions);
}

/** Get all missions for a given level */
export function getMissionsByLevel(level: Mission["level"]): Mission[] {
  return scenarios.flatMap((s) => s.missions).filter((m) => m.level === level);
}
